    arr = []
    while len(arr) < 10:
        number = random.randint(0, 9)
        # –Ø–∫—â–æ –∑–≥–µ–Ω–µ—Ä–æ–≤–∞–Ω–µ —á–∏—Å–ª–æ —É–∂–µ —î –≤ —Å–ø–∏—Å–∫—É, —Ç–æ –ø–µ—Ä–µ–≥–µ–Ω–µ—Ä–æ–≤—É—î–º–æ –π–æ–≥–æ
